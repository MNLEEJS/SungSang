public class Variables {
	public static void main(String[] args){
		// 변수  (Variable)  >> 값을 보관하는 형태 변
		// 변수 선언 (declare)
		// int (정수 자료형)
		int a;   // 자료형(datatype)과 식별자(identifier)
		int b;
		// 대입 연산자 (=)		
		a = 100;  // 초기화 (intialize)
		b = 200;
		
		System.out.println(a + b);
		
		a = 300 ;
		
		System.out.println(a + b);
	}
}