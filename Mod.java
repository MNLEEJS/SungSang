public class Mod {
	public static void main(String[] args){
		int num1 = 10;
		int num2 = 4;
		// 나누기의 몫 / 
		System.out.println(num1 / num2);
		// 나누기의 나머지 %
		System.out.println(num1 % num2);
		
		num2 = 3;
		System.out.println(num1 % num2);
		num2 = 5;
		System.out.println(num1 % num2);
	}
}